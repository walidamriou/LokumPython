# The declaration of the variables in Python like that:  <variable name> = <value>

# Integer
a = 2
print(a)

# Integer
b = 9223372036854775807
print(b)

# Floating point
pi = 3.14
print(pi)

# String (but like char)
c = 'A'
print(c)

# String
name = 'Lokum Python'
print(name)

# Boolean
d = True
print(d)

# Empty value or null data type
e = None
print(e)

print(' ')


